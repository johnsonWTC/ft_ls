#include "ft_ls.h"

char	*ft_group(char *tim)
{
	int		i;
	int		j;
	char	*s;

	s = (char *)malloc(sizeof(char) * 16);
	i = 3;
	j = 0;
	while (i < 16)
	{
		s[j] = tim[i];
		i++;
		j++;
	}
	return (s);
}
